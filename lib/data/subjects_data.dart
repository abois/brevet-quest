  static Subject? byId(String id) {
    for (final s in all) {
      if (s.id == id) return s;
    }
    return null;
  }

  /// Toutes les questions, tous chapitres confondus, filtrées par type.
  static List<Question> allQuestionsByType(QuestionType type) {
    return [
      for (final s in all)
        for (final c in s.chapters)
          for (final q in c.questions)
            if (q.type == type) q,
    ];
  }

  /// Variante filtrée par niveau de programme (4e / 3e / tous).
  static List<Question> allQuestionsByTypeForNiveau(
    QuestionType type,
    Niveau niveau,
  ) {
    return [
      for (final s in all)
        for (final c in s.chapters)
          if (c.matchesUserNiveau(niveau))
            for (final q in c.questions)
              if (q.type == type) q,
    ];
  }

